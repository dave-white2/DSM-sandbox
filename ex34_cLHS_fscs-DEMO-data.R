#Area (Convex Hull): Shows how much of the "extreme" feature space each method captured. cLHS usually wins here because its algorithm specifically hunts for the edges of the distribution.

#Avg_Spacing: Measures how spread out the points are. A higher number means the points are more dispersed (common in Centroid sampling), while a lower number indicates points are closer together (common in Stratified if a cluster is small).

