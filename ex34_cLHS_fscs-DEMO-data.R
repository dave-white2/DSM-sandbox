# 3. Create a function to calculate all 4 comparison metrics
calc_all_metrics <- function(pts_df, pop_means, pop_sds) {
  # A. Convex Hull Area (Extent of Coverage)
  area <- convhulln(pts_df, options = "FA")$vol
  
  # B. Mean Nearest Neighbor Distance (Spread/Uniformity)
  avg_dist <- mean(nndist(pts_df[,1], pts_df[,2]))
  
  # C. Representativeness Score (Euclidean distance between Sample Mean and Pop Mean)
  # Lower is better (0 = perfect representation of the center)
  sample_means <- colMeans(pts_df)
  rep_score <- sqrt(sum((sample_means - pop_means)^2))
  
  # D. Variance Capture (Ratio of Sample SD to Pop SD)
  # Close to 1 means the sample captures the same "width" as the population
  sample_sds <- apply(pts_df, 2, sd)
  var_ratio <- mean(sample_sds / pop_sds)
  
  return(c(
    Area_Covered = round(area, 2), 
    Uniformity = round(avg_dist, 2), 
    Bias_Score = round(rep_score, 4), 
    Variance_Ratio = round(var_ratio, 3)
  ))
}

