#What do these new metrics mean? 

#Area_Covered: Look for the highest number to see which method captured the most "environmental space." (Usually cLHS).

#Bias_Score: A higher number here means the sample is further from the population average (less representative of the "typical" pixel).
#Bias Score (Representativeness): This is the distance between the "center of gravity" of your sample and the "center of gravity" of the whole map.

# Ideally with large enough samples - FSCS-Stratified usually has the best (lowest) score because it samples proportional to density.cLHS might have a higher bias because it forces samples into the "tails" (extremes).

#Uniformity: Higher bars mean the points are more evenly spread out from each other (Usually FSCS-Centroid).

#Variance_Ratio: This shows which method is "boldest" in capturing the full width of the data distribution.
#Variance Ratio: This tells you if your sample is too "clumped" or "extreme."A ratio of 1.0 is a perfect match.> 1.0 means your sample is over-representing the extremes (typical of cLHS).< 1.0 means your sample is too conservative and staying too close to the average.

#Pro-Tip: The "Winner"There is no single "winner," but this table helps you justify your choice:Use cLHS if Area_Covered and Variance_Ratio are your priorities (best for mapping rare soils).Use FSCS-Centroid if Uniformity is the goal (best for general interpolation).Use FSCS-Stratified if Bias_Score is most important (best for calculating global averages).


